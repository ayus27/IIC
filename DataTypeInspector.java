public class DataTypeInspector{
public static void main (String [] args){
 
    boolean b=true;
    byte a= 10;
    short c= 50;
    int i= 60;
    long l= 12L;
    float f= 30.3f;
    double d= 22.5d;
    char ch= 'A';
    
    System.out.println("The value of boolean is " + b);
    System.out.println("The value of byte is " + 10);
    System.out.println("The value of short is " + 50);
    System.out.println("The value of int is " + i);
    System.out.println("The value of long is " + l);
    System.out.println("The value of float is " + f);
    System.out.println("The value of double is " + d);
    System.out.println("The value of character is " +ch);
    
    
    
}
}